


from collections import deque

# Create an empty deque
q = deque()

# Add elements to the deque
q.append(1)  # Adds 1 to the right end of the deque
q.append(2)  # Adds 2 to the right end
q.append(3)  # Adds 3 to the right end

# Print the deque
print(q)  # Output: deque([1, 2, 3])
